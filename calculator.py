a = float(input("ENTER THE FIRST NUMBER\t"))
b = float(input("ENTER THE SECOND NUMBE\t"))
c = input("ENTER THE OPERATOR +,-,*,/\t")
if(c=="+"):
    print(a+b)
elif(c=="-"):
    print(a-b)  
elif(c=="*"):
    print((a*b))      
elif(c=="/"):
    print(a/b)
else:
    print("THE OPERATER IS NOT PRESENT")    